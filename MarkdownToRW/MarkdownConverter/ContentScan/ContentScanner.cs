        public static ScanResults ScanMarkdown(string markdownText, string markdownPath)
        {
            ScanResults results = new ScanResults();

            results.Filename = Path.GetFileName(markdownPath);
            
            CountWords(ref results, markdownText);
            ScanSectionTitles(ref results, markdownText);
            ScanSmileys(ref results, markdownText);
            //ScanImages(ref results, markdownText, markdownPath);
            ScanWords(ref results, markdownText);
            
            return results;
        }

        public static string ParseScanrResults(ScanResults results)
        {
            string output = "Markdown Report For " + results.Filename + "\n";
            output += "-------------\n";
            output += "Word Count: " + results.WordCount;
            output += "\n\n";
            output += "Potential problems found:\n";

            foreach (string p in results.ProblemsFound)
            {
                output += "- " + p + "\n";
            }
            
            return output;
        }

        private static void CountWords(ref ScanResults results, string text)
        {
            char[] delimiters = GetDelimiters();
            results.WordCount = text.Split(delimiters, StringSplitOptions.RemoveEmptyEntries).Length;

            if (results.WordCount > 4000)
            {
                results.ProblemsFound.Add("Your text contains " + results.WordCount +" words. This is above the maximum word limit of 4000.");
            }
        }

        private static void ScanSectionTitles(ref ScanResults results, string markdown)
        {
            List<string> neededSections = new List<string>();
            neededSections.Add("Getting Started");
            neededSections.Add("Where to Go From Here");

            foreach (string neededSection in neededSections)
            {
                if (!markdown.Contains(neededSection))
                {
                    results.ProblemsFound.Add("Section not found or spelled/capitalized incorrectly: " + neededSection);
                }
            }
        }

        private static void ScanSmileys(ref ScanResults results, string markdown)
        {
            List<string> badSmileys = new List<string>();
            badSmileys.Add(" :)");
            badSmileys.Add(" =]");
            badSmileys.Add(" =)");
            badSmileys.Add(" :D");
            badSmileys.Add(" =D");
            badSmileys.Add(" :p");
            badSmileys.Add(" ^_^");
            badSmileys.Add(" =p");
            badSmileys.Add(" >_<");
            badSmileys.Add(" >_>");
            badSmileys.Add(" <_<");

            foreach (string badSmiley in badSmileys)
            {
                if (markdown.Contains(badSmiley))
                {
                    results.ProblemsFound.Add("Bad smiley found: " + badSmiley + " Please use this one instead: :]");
                }
            }
        }

        // Not needed anymore
        private static void ScanImages(ref ScanResults results, string markdownText, string markdownPath)
        {
            string html = Converter.ConvertMarkdownStringToHtml(markdownText);
            string rootPath = Path.GetDirectoryName(markdownPath);

            HtmlDocument doc = new HtmlDocument();
            doc.LoadHtml(html);
            HtmlNodeCollection imgNodes = doc.DocumentNode.SelectNodes("//img[@src]");

            if (imgNodes == null || imgNodes.Count == 0)
            {
                return;
            }

            var links = Converter.FindAllImageLinksInHtml(html, rootPath);

            for (var i = 0; i < links.Count; i++)
            {
                ImageLinkData link = links[i];

                var size = ImageHelper.GetDimensions(link.FullImagePath);


                if (i == 0)
                {
                    if (size.Width != 250 && size.Height != 250)
                    {
                        results.ProblemsFound.Add(link.FullImagePath + ": first image's size isn't 250x250, it's " + size.Width + "x" + size.Height + " instead. (not necessary as it will be replaced by the illustrator)");
                    }
                }

                if (size.Width > 700)
                {
                    results.ProblemsFound.Add(link.FullImagePath + ": image width larger than 700, it's " + size.Width + "x" + size.Height + ". You'll need to manually edit the size class to 'large' in the HTML after uploading. (a future update to this app will do this for you)");
                }
            }
        }

        private static void ScanWords(ref ScanResults results, string markdownText)
        {
            char[] delimiters = GetDelimiters();
            string[] words = markdownText.Split(delimiters, StringSplitOptions.RemoveEmptyEntries);

            Dictionary<string, string> britshAmericanWordsDict = CreateBritishWordDict();
            Dictionary<string, string> commonMisspelledWordsDict = CreateCommonMisspelledWordDict();

            foreach (string word in words)
            {
                if (britshAmericanWordsDict.ContainsKey(word.ToLower()))
                {
                    string american = britshAmericanWordsDict[word];
                    results.ProblemsFound.Add("British word found: '" + word + "'. Please replace this with '" + american + "'.");
                }

                if (commonMisspelledWordsDict.ContainsKey(word.ToLower()))
                {
                    string correctSpelling = commonMisspelledWordsDict[word];
                    results.ProblemsFound.Add("Incorrectly spelled word found: '" + word + "'. Please replace this with '" + correctSpelling + "'.");
                }
            }
        }

        private static char[] GetDelimiters()
        {
            return new[] { ' ', '\r', '\n', '*', '_', '[', ']', '(', ')', '<', '>', '.', ',', ';', '—', '#', '!', '/', '\\' ,'&','-',':','\''};
        }

